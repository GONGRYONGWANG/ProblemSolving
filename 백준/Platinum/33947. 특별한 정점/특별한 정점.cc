vector<int> E[200001];
int special[200001];
int parent[200001];

int ans = 0;

int dfs(int x) {
    int cnt = 0;
    for (int nx : E[x]) {
        if (nx == parent[x]) continue;
        parent[nx] = x;
        cnt += dfs(nx);
    }
    if (cnt >= 2) {
        ans += cnt - 1;
        return 0;
    }
    if (cnt == 0 && !special[x]) return 0;
    return 1;
}

void solve(int tc) {

    int N;
    cin >> N;
    
    for (int i = 0; i < N - 1; i++) {
        int u, v;
        cin >> u >> v;
        E[u].push_back(v);
        E[v].push_back(u);
    }

    for (int i = 1; i <= N; i++)  cin >> special[i];

    if (dfs(1)) ans += 1;

    cout << max(0, ans - 1);




}
    



