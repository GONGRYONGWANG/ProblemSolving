void solve(int tc) {

    int N, K;
    cin >> N >> K;

    vector<int> idx(K + 1, 0);
    vector<int> nx(K + 1, inf);
    vector<int> arr(K + 1);
    for (int i = 1; i <= K; i++) {
        cin >> arr[i];
        if (idx[arr[i]]) nx[idx[arr[i]]] = i;
        idx[arr[i]] = i;
    }

    int ans = 0;
    multiset<int> st;
    for (int i = 1; i <= K; i++) {
        if (st.size() == N && *st.begin() != i) {
            st.erase(prev(st.end()));
            ans += 1;
        }
        if (!st.empty() && *st.begin() == i) st.erase(st.begin());
        st.insert(nx[i]);
    }

    cout << ans;

}


int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int T = 1;
    //cin >> T;
    for (int tc = 1; tc <= T; tc++) {
        solve(tc);
    }

    return 0;
}