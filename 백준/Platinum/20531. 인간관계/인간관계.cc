ll comb[5001][5001];
ll mod = 1e9 + 7;
ll DP[5001];

int parent[5001];
int findp(int x) {
    if (parent[x] == x) return x;
    return parent[x] = findp(parent[x]);
}

void solve(int tc) {

    int N, M;
    cin >> N >> M;

    for (int i = 1; i <= N; i++) {
        parent[i] = i;
    }

    for (int i = 0; i <= N; i++) {
        comb[i][0] = 1;
        for (int j = 1; j <= i; j++) {
            comb[i][j] = (comb[i - 1][j] + comb[i - 1][j - 1]) % mod;
        }
    }

    DP[0] = 1;
    for (int i = 1; i <= N; i++) {
        for (int j = 0; j <= i - 1; j++) {
            DP[i] += comb[i - 1][j] * DP[i - 1 - j];
            DP[i] %= mod;
        }
    }


    int sz = N;

    while (M--) {
        int x, y;
        cin >> x >> y;
        if (findp(x) != findp(y)) {
            sz -= 1;
            parent[findp(x)] = findp(y);
        }
        cout << DP[sz] << endl;
    }






    // cout << "Case #" << tc << ": " << ret<< endl;
}

int main() {
    //fin.open("input.txt"); fout.open("output.txt");
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int T = 1;
    //cin >> T;
    for (int tc = 1; tc <= T; tc++) {
        solve(tc);
    }

    return 0;
}