int DP[101][100];
int DP2[101][100];

void solve(int tc) {

    int N;
    cin >> N;

    int ans = 0;

    for (int i = 0; i < N; i++) {
        int x;
        cin >> x;
        for (int j = 0; j < 100; j++) {
            if (x - j > 0) DP[x][j] = DP[x - j][j] + 1;
            else DP[x][j] = 1;

            if (x + j <= 100) DP2[x][j] = DP2[x + j][j] + 1;
            else DP2[x][j] = 1;

            ans = max(ans, DP[x][j]);
            ans = max(ans, DP2[x][j]);
        }
    }

    cout << ans;
    

}


int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int T = 1;
    //cin >> T;
    for (int tc = 1; tc <= T; tc++) {
        solve(tc);
    }

    return 0;
}