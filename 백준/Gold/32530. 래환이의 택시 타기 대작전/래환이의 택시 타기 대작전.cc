void solve(int tc) {

    int N;
    cin >> N;
    vector<int> arr(N);
    for (int i = 0; i < N; i++) {
        int h, m; char tmp;
        cin >> h >> tmp >> m;
        arr[i] = h * 60 + m;
    }

    sort(arr.begin(), arr.end());

    int ret = 1;
    int cnt = 0;
    int s = inf;
    for (int x : arr) {
        if (cnt == 3 || s + 20 < x) {
            ret += 1;
            cnt = 0;
            s = inf;
        }

        if (cnt == 0) s = x;

        cnt += 1;
    }

    cout << ret;





}
    



