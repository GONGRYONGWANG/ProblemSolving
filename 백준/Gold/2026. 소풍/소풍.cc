int N, K;

bool adj[901][901];


vector<int> v;
bool bt(int x) {
    if (v.size() == K) {
        for (int u : v) {
            cout << u << endl;
        }
        return true;
    }
    if (x == N + 1) return false;
    bool flag = true;
    for (int u : v) {
        if (!adj[u][x]) flag = false;
    }
    if (flag) {
        v.push_back(x);
        if (bt(x + 1)) return true;
        v.pop_back();
    }
    if (bt(x + 1)) return true;
    return false;
}


void solve(int tc) {

    int F;
    cin >> K >> N >> F;

    while (F--) {
        int u, v;
        cin >> u >> v;
        adj[u][v] = adj[v][u] = true;
    }

    if (!bt(1)) cout << -1;


}
    



int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int T = 1;
    //cin >> T;
    //fin.open("input.txt"); fout.open("output.txt");
    for (int tc = 1; tc <= T; tc++) {
        solve(tc);
    }

    return 0;
}