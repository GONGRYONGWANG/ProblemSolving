int parent[100001];
int sz[100001];
int findp(int x) {
    if (parent[x] == x) return x;
    return parent[x] = findp(parent[x]);
}

void solve(int tc) {

    int N, M;
    cin >> N >> M;

    for (int i = 1; i <= N; i++) parent[i] = i, sz[i] = 1;

    while (M--) {
        int u, v;
        cin >> u >> v;
        u = findp(u); v = findp(v);
        if (u == v) continue;
        sz[u] += sz[v];
        parent[v] = u;
    }

    int C, H, K;
    cin >> C >> H >> K;
    C = findp(C); H = findp(H);

    vector<int> v;
    for (int i = 1; i <= N; i++) {
        int u = findp(i);
        if (u != i) continue;
        if (u == C || u == H) continue;
        v.push_back(sz[u]);
    }

    sort(v.rbegin(), v.rend());

    int ans = sz[C];
    for (int i = 0; i < min(K, int(v.size())); i++) {
        ans += v[i];
    }

    cout << ans;








}
    



int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int T = 1;
    //cin >> T;
    //fin.open("input.txt"); fout.open("output.txt");
    for (int tc = 1; tc <= T; tc++) {
        solve(tc);
    }

    return 0;
}